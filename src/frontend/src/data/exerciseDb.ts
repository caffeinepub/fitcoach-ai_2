export function getExercisesByMuscle(muscle: MuscleGroup): ExerciseInfo[] {
  return EXERCISES.filter((e) => e.primaryMuscle === muscle);
}

export function getExercisesByEquipment(equip: Equipment[]): ExerciseInfo[] {
  if (equip.length === 0) return EXERCISES;
  return EXERCISES.filter((e) => e.equipment.some((eq) => equip.includes(eq)));
}

export function getExerciseById(id: string): ExerciseInfo | undefined {
  return EXERCISES.find((e) => e.id === id);
}

